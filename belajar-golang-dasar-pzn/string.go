package main

import "fmt"

func main() {
	fmt.Println("Alroy")
	fmt.Println("Alroy Rasyid")
	fmt.Println("Alroy Rasyid Resan")

	//menghitung jumlah karakter pada string
	fmt.Println(len("Alroy"))
	fmt.Println(len("Alroy Rasyid"))
	fmt.Println(len("Alroy Rasyid Resan"))

	//mengambil karakter pada index
	fmt.Println("Alroy"[1])
	fmt.Println("Alroy Rasyid"[2])
	fmt.Println("Alroy Rasyid Resan"[3])
}
